package com.company;

public class FormatString {

    public static void main(String[] args) {
        System.out.printf("String %10d \n", 43);
        System.out.printf("String %10.2f \n", 42.56784);
        System.out.printf("String %10.4f \n", 14.45373);
        System.out.printf("String %10s \n", "Steve");
    }
}
